////////////////////////////////////////////////////////////////////////////////
// Anropas 100 gånger per sekund. Utför all logik här. 
////////////////////////////////////////////////////////////////////////////////
void Gameover::Update() {

	if (counter == 800) {
		GameApp()->SetScreen(new Gamescreen());
	}
	counter = counter + 1;
}

